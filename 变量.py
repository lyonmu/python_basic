# 双引号和单引号的工作机制一样
def test():
    # int类型可以表示无穷大的数字
    variable1 = 123
    variable2 = 11111111111111111111111111111111111111111111111111
    variable3 = 123.23
    variable4 = 'string'
    # type 函数可以获取变量的类型
    print(type(variable1))
    print(type(variable2))
    print(type(variable3))
    print(type(variable4))


if __name__ == '__main__':
    test()

